public class Tiger implements CatFamily {
    //必须实现CatFamily中的三个方法
    public void eat() {
        System.out.println("Tiger: I can eat");
    }

    public void move() {
        System.out.println("Tiger: I can move");
    }

    public void climb() {
        System.out.println("Tiger: I can climb");
    }
}
